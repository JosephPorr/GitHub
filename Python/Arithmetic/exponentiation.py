# A ** (double asterisk) sign is an exponentiation (power) operator. 
# Its left argument is the base, its right, the exponent.

print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

# Classical mathematics prefers notation with superscripts, just like this: 2^3. 
# Pure text editors don't accept that, so Python uses ** instead, e.g., 2 ** 3.